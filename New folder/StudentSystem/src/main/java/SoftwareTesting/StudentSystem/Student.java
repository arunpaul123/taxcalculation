package SoftwareTesting.StudentSystem;

public class Student {
	
	String firstname;
	String lastname;
	String gender;
	String address;
	String studentId;
	double totalMark;
	double finalMark;
	String grade;
	double assignment1;
	double assignment2;
	double assignment3;
	Boolean passOrNot;
	public String getFirstname() {
		return firstname;
	}
	public void setFirstname(String firstname) {
		this.firstname = firstname;
	}
	public String getLastname() {
		return lastname;
	}
	public void setLastname(String lastname) {
		this.lastname = lastname;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getStudentId() {
		return studentId;
	}
	public void setStudentId(String studentId) {
		this.studentId = studentId;
	}
	public double getTotalMark() {
		return totalMark;
	}
	public void setTotalMark(double totalMark) {
		this.totalMark = totalMark;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	public double getAssignment1() {
		return assignment1;
	}
	public void setAssignment1(double assignment1) {
		this.assignment1 = assignment1;
	}
	public double getAssignment2() {
		return assignment2;
	}
	public void setAssignment2(double assignment2) {
		this.assignment2 = assignment2;
	}
	public double getAssignment3() {
		return assignment3;
	}
	public void setAssignment3(double assignment3) {
		this.assignment3 = assignment3;
	}
	public Boolean getPassOrNot() {
		return passOrNot;
	}
	public void setPassOrNot(Boolean passOrNot) {
		this.passOrNot = passOrNot;
	}
	public double getFinalMark() {
		return finalMark;
	}
	public void setFinalMark(double finalMark) {
		this.finalMark = finalMark;
	}
	
	
	
	
	
}
